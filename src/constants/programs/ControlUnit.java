     public String 아이피(String name) throws SQLException {
         String a = "";
         PreparedStatement ps2 = MYSQL.getConnection().prepareStatement("SELECT * FROM accounts WHERE name = ?");
         ps2.setString(1, name);
         ResultSet con = ps2.executeQuery();
         while(con.next()) {
             a = con.getString("ip");
         }
         ps2.close();
         con.close();
         return a;
    }
    
